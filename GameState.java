package tictactoe;

/**
 * The GameState enumeration represents the two states the game can be in.
 *
 * @version 1.10 01 Sep 2023
 * @author Clayton Mercer
 */
enum GameState {

    /**
     * Game is not operational.
     */
    OFF,

    /**
     * Game is currently ongoing.
     */
    PLAYING,
}
